make.bonacich <- function() {
   g1 <- make_empty_graph(10, directed = FALSE)
   g1 <- g1 + edge(1,2)
   g1 <- g1 + edge(1,3)
   g1 <- g1 + edge(1,9)
   g1 <- g1 + edge(2,3)
   g1 <- g1 + edge(2,10)
   g1 <- g1 + edge(3,4)
   g1 <- g1 + edge(4,5)
   g1 <- g1 + edge(4,6)
   g1 <- g1 + edge(5,10)
   g1 <- g1 + edge(5,6)
   g1 <- g1 + edge(6,7)
   g1 <- g1 + edge(7,8)
   g1 <- g1 + edge(7,9)
   g1 <- g1 + edge(8,10)
   g1 <- g1 + edge(8,9) #Bonacich (2007) regular network Fig. 5
   g2 <- make_empty_graph(12, directed = FALSE)
   g2 <- g2 + edge(1,2)
   g2 <- g2 + edge(1,6)
   g2 <- g2 + edge(1,7)
   g2 <- g2 + edge(2,3)
   g2 <- g2 + edge(2,10)
   g2 <- g2 + edge(3,4)
   g2 <- g2 + edge(3,8)
   g2 <- g2 + edge(4,5)
   g2 <- g2 + edge(4,11)
   g2 <- g2 + edge(5,6)
   g2 <- g2 + edge(5,9)
   g2 <- g2 + edge(6,12)
   g2 <- g2 + edge(7,8)
   g2 <- g2 + edge(7,9)
   g2 <- g2 + edge(8,9)
   g2 <- g2 + edge(10,11)
   g2 <- g2 + edge(10,12)
   g2 <- g2 + edge(11,12)
   return(list(g1 = g1, g2 = g2))
}